const STORAGE_KEY = 'site-lang';

function getStoredLang(): Lang {
  try {
    const s = localStorage.getItem(STORAGE_KEY);
    if (s === 'ar' || s === 'en') return s;
  } catch (_) {}
  return 'en';
}

export let currentLang: Lang = getStoredLang();

function setDocumentDirection(lang: Lang) {
  const html = document.documentElement;
  html.lang = lang === 'ar' ? 'ar' : 'en';
  html.dir = lang === 'ar' ? 'rtl' : 'ltr';
}

function getNested(obj: Record<string, unknown>, key: string): string | undefined {
  const parts = key.split('.');
  let v: unknown = obj;
  for (const p of parts) {
    if (v == null || typeof v !== 'object') return undefined;
    v = (v as Record<string, unknown>)[p];
  }
  return typeof v === 'string' ? v : undefined;
}

export function getTranslation(key: string): string {
  const t = translations[currentLang] as Record<string, unknown>;
  return getNested(t, key) ?? key;
}

export function applyLanguage(lang: Lang) {
  currentLang = lang;
  try {
    localStorage.setItem(STORAGE_KEY, lang);
  } catch (_) {}
  setDocumentDirection(lang);

  const t = translations[lang] as Record<string, unknown>;

  document.querySelectorAll<HTMLElement>('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (!key) return;
    const value = getNested(t, key);
    if (value != null) el.textContent = value;
  });

  document.querySelectorAll<HTMLElement>('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (!key) return;
    const value = getNested(t, key);
    if (value != null && 'placeholder' in el) (el as HTMLInputElement).placeholder = value;
  });

  const toggleBtn = document.getElementById('lang-toggle');
  if (toggleBtn) {
    toggleBtn.textContent = lang === 'en' ? translations.en.langToggle.switchToAr : translations.ar.langToggle.switchToEn;
  }
}

export function initI18n() {
  applyLanguage(currentLang);
}
